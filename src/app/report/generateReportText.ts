  if (language !== "ar") return "";

  const descMap =
    surveyType === "explorers"
      ? explorerModalDescriptions
      : surveyType === "entrepreneurs"
      ? entrepreneurModalDescriptions
      : companiesModalDescriptions;

  // Sort modals by score
  const sorted = [...modalScores].sort((a, b) => b.score - a.score);
  const top3 = sorted.slice(0, 3);
  const bottom3 = sorted.slice(-3).reverse();

  const { level, summary, recs } = getScoreLevelAr(percentage, surveyType);

  // ── Intro ──
  const intro =
    surveyType === "explorers"
      ? `نشكر لكم اهتمامكم بالمشاركة في اختبار قياس الجاهزية التجارية لمستكشف السوق.\nهذا التقييم لا يهدف إلى الحكم على مشروع محدد أو فكرة قائمة، وإنما صُمّم لقياس مستوى الجاهزية الشخصية والمهنية للدخول إلى عالم السوق، وفهم الفرص، واستكشاف المسارات التجارية المناسبة بصورة أكثر وعيًا واتزانًا.`
      : surveyType === "entrepreneurs"
      ? `نشكر لكم اهتمامكم بالمشاركة في اختبار قياس القوة التجارية.\nوقد صُمّم هذا التقييم لقياس مجموعة من المهارات والقدرات التي تؤثر مباشرة في نجاح رائد الأعمال، وفي قدرته على إدارة العمل، واتخاذ القرارات، وتطوير النشاط التجاري على أسس أكثر وعيًا وتنظيمًا.`
      : `نشكر لكم اهتمامكم بالمشاركة في اختبار قياس الأداء المؤسسي.\nهذا التقييم صُمّم لقياس مستوى النضج المؤسسي في مختلف جوانب العمل التجاري، وتحديد مواطن القوة والفرص التطويرية بصورة شاملة ومنهجية.`;

  // ── Score ──
  const scoreNote =
    surveyType === "entrepreneurs"
      ? `وقد أظهرت نتيجتكم ${totalScore} من 360 بنسبة ${percentageStr}%، وهي نتيجة تعكس ${level}.`
      : `وقد أظهرت النتيجة العامة ${totalScore} من 360 بنسبة ${percentageStr}%، وهي نتيجة تعكس ${level}.`;

  // ── Strengths ──
  const strengthsTitle =
    surveyType === "entrepreneurs"
      ? "ومن أبرز عناصر القوة التي ظهرت في التقييم:"
      : "من أبرز عناصر القوة التي ظهرت في التقييم:";

  const strengthLines =
    surveyType === "explorers" || surveyType === "companies"
      ? top3
          .map((item, i) => {
            const ordinal = ["أولًا", "ثانيًا", "ثالثًا"][i];
            const name = getModalName(item.modalId);
            const desc = descMap[item.modalId]?.asStrength || "";
            return `${ordinal}: ${name}، ${desc}`;
          })
          .join("\n")
      : // entrepreneurs style
        top3
          .map((item, i) => {
            const name = getModalName(item.modalId);
            const desc = descMap[item.modalId]?.asStrength || "";
            if (i === 0) return `${name}، ${desc}`;
            if (i === 1) return `كما برزت قوة في ${name}، ${desc}`;
            return `كذلك ظهر تميز في ${name}، ${desc}`;
          })
          .join("\n");

  // ── Weaknesses ──
