export const getPrograms = (locale: string) => {
  if (locale === "en") {
    return programsDataEn;
  }
  return programsDataAr;
}

export const getProgram = (id: string, locale: string) => {
  if (locale === "en") {
    return programsDataEn.find((program) => program.id === id);
  }
  return programsDataAr.find((program) => program.id === id);
}